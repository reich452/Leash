import UIKit

extension UIColor {
    
    /// Only available on the class UIColor. Main purple theme through out
    static func leashPurple(red: CGFloat, green: CGFloat, blue: CGFloat) -> UIColor {
        return UIColor(red: 117.0/255.0, green: 91.0/255.0, blue: 208.0/255.0, alpha: 1.0)
    }
}
